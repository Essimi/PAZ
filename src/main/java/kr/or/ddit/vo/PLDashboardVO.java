package kr.or.ddit.vo;

public class PLDashboardVO {
	
	//업무 담당자별 할당 
	private String memId;
	private String memNickname;
	private String taskCount;
	//이슈 받은건 & 요청건
	private String recipientCount;
	private String issueCount;
	private String pCode;
	//긴급업무 요청
	private String importanceName;
	private String workName;
	private String progressName;

	//팀원별 업무 상태 그래프
	private String todo;
	private String process;
	private String done;
	
	//팀원별 근무시간 그래프
	private String monday;
	private String tuesday;
	private String wednesday;
	private String thursday;
	private String friday;
	private String total;
	
	
	
	public String getMonday() {
		return monday;
	}
	public void setMonday(String monday) {
		this.monday = monday;
	}
	public String getTuesday() {
		return tuesday;
	}
	public void setTuesday(String tuesday) {
		this.tuesday = tuesday;
	}
	public String getWednesday() {
		return wednesday;
	}
	public void setWednesday(String wednesday) {
		this.wednesday = wednesday;
	}
	public String getThursday() {
		return thursday;
	}
	public void setThursday(String thursday) {
		this.thursday = thursday;
	}
	public String getFriday() {
		return friday;
	}
	public void setFriday(String friday) {
		this.friday = friday;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getTodo() {
		return todo;
	}
	public void setTodo(String todo) {
		this.todo = todo;
	}
	public String getProcess() {
		return process;
	}
	public void setProcess(String process) {
		this.process = process;
	}
	public String getDone() {
		return done;
	}
	public void setDone(String done) {
		this.done = done;
	}
	public String getImportanceName() {
		return importanceName;
	}
	public void setImportanceName(String importanceName) {
		this.importanceName = importanceName;
	}
	public String getWorkName() {
		return workName;
	}
	public void setWorkName(String workName) {
		this.workName = workName;
	}
	public String getProgressName() {
		return progressName;
	}
	public void setProgressName(String progressName) {
		this.progressName = progressName;
	}
	public String getRecipientCount() {
		return recipientCount;
	}
	public void setRecipientCount(String recipientCount) {
		this.recipientCount = recipientCount;
	}
	public String getIssueCount() {
		return issueCount;
	}
	public void setIssueCount(String issueCount) {
		this.issueCount = issueCount;
	}
	public String getpCode() {
		return pCode;
	}
	public void setpCode(String pCode) {
		this.pCode = pCode;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getMemNickname() {
		return memNickname;
	}
	public void setMemNickname(String memNickname) {
		this.memNickname = memNickname;
	}
	public String getTaskCount() {
		return taskCount;
	}
	public void setTaskCount(String taskCount) {
		this.taskCount = taskCount;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((memId == null) ? 0 : memId.hashCode());
		result = prime * result + ((memNickname == null) ? 0 : memNickname.hashCode());
		result = prime * result + ((taskCount == null) ? 0 : taskCount.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PLDashboardVO other = (PLDashboardVO) obj;
		if (memId == null) {
			if (other.memId != null)
				return false;
		} else if (!memId.equals(other.memId))
			return false;
		if (memNickname == null) {
			if (other.memNickname != null)
				return false;
		} else if (!memNickname.equals(other.memNickname))
			return false;
		if (taskCount == null) {
			if (other.taskCount != null)
				return false;
		} else if (!taskCount.equals(other.taskCount))
			return false;
		return true;
	}
	@Override
	public String toString() {
		return "PLDashboardVO [memId=" + memId + ", memNickname=" + memNickname + ", taskCount=" + taskCount + "]";
	}
	
	
	
}
